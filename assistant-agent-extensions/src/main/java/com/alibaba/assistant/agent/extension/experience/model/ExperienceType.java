package com.alibaba.assistant.agent.extension.experience.model;

/**
 * 经验类型枚举
 *
 * @author Assistant Agent Team
 */
public enum ExperienceType {

    /**
     * 代码经验 - 包含代码片段、风格规范与最佳实践
     */
    CODE,

    /**
     * React经验 - Agent行为经验与策略建议
     */
    REACT,

    /**
     * 通用常识经验 - 规范、注意事项、安全提示等
     */
    COMMON
}
